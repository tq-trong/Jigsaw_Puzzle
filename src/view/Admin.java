    public void loadData() {
        Connection conn = ConnectionDB.getConnection();
        try {
            String query = "select GameID, MoTa from Game";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                String id = String.valueOf(rs.getInt("GameID"));
                String mt = rs.getString("MoTa");

                String tbData[] = {id, mt};
                model = (DefaultTableModel) tbGame.getModel();
                model.addRow(tbData);
            }

            tbGame.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent lse) {
                    if (tbGame.getSelectedRow() >= 0) {
                        txtName.setText(tbGame.getValueAt(tbGame.getSelectedRow(), 1) + "");
                    }
                }
            });
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

