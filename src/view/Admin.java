    String[] image = new String[15];

    void setArrayNull() {
        for (int i = 0; i < 15; i++) {
            image[i] = null;
        }
    }
    private void btnImgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImgActionPerformed
        String img = "";
        File[] selectedFile = null;
        JFileChooser chooser = new JFileChooser();
        chooser.setMultiSelectionEnabled(true);
        chooser.showOpenDialog(this);
        selectedFile = chooser.getSelectedFiles();

        setArrayNull();
        int i = 0;
        if (selectedFile.length == 15) {
            for (File file : selectedFile) {
                String absolutePath = file.getAbsolutePath(); //Lấy đường dẫn
                Path p = Paths.get(absolutePath);
                String fileS = p.getFileName().toString(); //Lấy tên file
                img += fileS + "\n";

                image[i] = fileS;
                //System.out.println(image[i]);
                i++;
            }
            txtaImg.setText(img);
        } else {
            txtaImg.setText("");
            JOptionPane.showMessageDialog(this, "You must select 15 images");
        }
    }//GEN-LAST:event_btnImgActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        GameDAO gd = new GameDAO();
        if (txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Description not be blank");
        } else if (gd.updateGame(txtName.getText(), Integer.parseInt(tbGame.getValueAt(tbGame.getSelectedRow(), 0) + "")) > 0) {
            JOptionPane.showMessageDialog(this, "Updated");
            dispose();
            new Admin().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "No row to be selected");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        GameDAO gd = new GameDAO();
        try {
            if (gd.deleteGame(Integer.parseInt(tbGame.getValueAt(tbGame.getSelectedRow(), 0) + "")) > 0) {
                JOptionPane.showMessageDialog(this, "Deleted");
                dispose();
                new Admin().setVisible(true);
            } else if (gd.deleteGame(Integer.parseInt(tbGame.getValueAt(tbGame.getSelectedRow(), 0) + "")) < 0) {
                JOptionPane.showMessageDialog(this, "Cannot Deleted");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No row to be selected");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

