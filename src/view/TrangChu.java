    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
        String oldpass = txtOPass.getText().trim();
        String newpass = txtNPass.getText().trim();
        String connewpass = txtCPass.getText().trim();
        int ce = JOptionPane.showConfirmDialog(this, "Do you want to change password now?", "Information", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if (ce == JOptionPane.YES_OPTION) {
            usd = new UserDAO();
            if(oldpass.equals("") || newpass.equals("") || connewpass.equals("")) {
                JOptionPane.showMessageDialog(this, "All fields not be blank");
            }
            else if (usd.checkToChange(user, oldpass) < 0) {
                JOptionPane.showMessageDialog(this, "Old Password is wrong");
            } else if (newpass.length() < 4 || newpass.length() > 20) {
                JOptionPane.showMessageDialog(this, "Length of New Password must >4 and <20");
            } else if (!newpass.equals(connewpass)) {
                JOptionPane.showMessageDialog(this, "New Password and Confirm Password does not match");
            } else {
                usd.changePassword(user, newpass);
                JOptionPane.showMessageDialog(this, "Change Password success");
            }
        }
    }//GEN-LAST:event_btnChangeActionPerformed

