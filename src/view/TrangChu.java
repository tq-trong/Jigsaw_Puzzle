    public void loadDataCB() {
        String sql = "Select * from Game";
        try {
            conn = ConnectionDB.getConnection();
            pstm = conn.prepareStatement(sql);
            ResultSet rs = pstm.executeQuery();
            
            while (rs.next()) {
                int id = rs.getInt("GameID");
                String name = rs.getString("MoTa");
                Games comboItem = new Games(id, name);
                cbxGame.addItem(comboItem.getDescription() + " "+comboItem.getGameID());

            }
  
        } catch (Exception e) {

        } finally {
            try {
                conn.close();
                pstm.close();
            } catch (Exception e) {

            }
        }
    }
    
